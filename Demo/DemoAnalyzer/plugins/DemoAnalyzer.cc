DemoAnalyzer::DemoAnalyzer(const edm::ParameterSet& iConfig)
 :
  tracksToken_(consumes<TrackCollection>(iConfig.getUntrackedParameter<edm::InputTag>("tracks")))

{
   //now do what ever initialization is needed

  std::cout << "LINE" << std::endl;
  smalltree = fs->make<TTree>("ttree", "ttree");
  smalltree->Branch("ntracks",    &ntracks,    "ntracks/I" );
  smalltree->Branch("tree_track_pt",    &tree_track_pt,    "tree_track_pt[ntracks]/D" );
  smalltree->Branch("tree_track_eta",   &tree_track_eta,   "tree_track_eta[ntracks]/D" );
  smalltree->Branch("tree_track_phi",   &tree_track_phi,   "tree_track_phi[ntracks]/D" );
}


